    private void browseForResource() throws URISyntaxException
    {
        File file = new File(editor.getAsText());
        fileChooser.setCurrentDirectory(file);

        int res = fileChooser.showOpenDialog(this);

        if (res != JFileChooser.APPROVE_OPTION)
        {
            return;
        }

        file = fileChooser.getSelectedFile();
        try {
//            editor.setAsText(file.getCanonicalPath());
            String path = file.toURI().toURL().toExternalForm();
            text_path.setText(path);
            editor.setAsText(path);
        } catch (IOException ex) {
            Logger.getLogger(ResourceCustomEditor.class.getName()).log(Level.SEVERE, null, ex);
        }
//        updateFromEditor();
    }

    private void updateFromEditorSwing()
    {
        updating = true;

        text_path.setText(editor.getAsText());

        updating = false;
    }

    private void setValue(String value)
    {
        URL url;
        try {
            url = new URL(value);
        } catch (MalformedURLException ex) {
            Logger.getLogger(URLCustomEditor.class.getName()).log(Level.SEVERE, null, ex);
            return;
        }

        curValue = value == null ? null
                : new PropertyDataInline<URL>(url);
        editor.setValue(curValue, false);
    }

    @Override
    public void customEditorCommit()
    {
        editor.setValue(curValue);
    }

    @Override
    public void customEditorCancel()
    {
        editor.setValue(initValue, false);
    }

    @Override
    public Component getCustomEditor()
    {
        return this;
    }

