package com.kitfox.rabbit.nodes;

/**
 *
 * @author kitfox
 */
public class RaFe extends RaElement
{
    public static final int IMAGE_SCRATCH = 0;
    public static final int IMAGE_SOURCEGRAPHIC = 1;
    public static final int IMAGE_SOURCEALPHA = 2;
    public static final int IMAGE_BACKGROUNDIMAGE = 3;
    public static final int IMAGE_BACKGROUNDALPHA = 4;
    public static final int IMAGE_FILLPAINT = 5;
    public static final int IMAGE_STROKEPAINT = 6;
    private float x;
    private float y;
    private float width;
    private float height;
    private int result;

    /**
     * @return the x
     */
    public float getX() {
        return x;
    }

    /**
     * @param x the x to set
     */
    public void setX(float x) {
        this.x = x;
    }

    /**
     * @return the y
     */
    public float getY() {
        return y;
    }

    /**
     * @param y the y to set
     */
    public void setY(float y) {
        this.y = y;
    }

    /**
     * @return the width
     */
    public float getWidth() {
        return width;
    }

    /**
     * @param width the width to set
     */
    public void setWidth(float width) {
        this.width = width;
    }

    /**
     * @return the height
     */
    public float getHeight() {
        return height;
    }

    /**
     * @param height the height to set
     */
    public void setHeight(float height) {
        this.height = height;
    }

    /**
     * @return the result
     */
    public int getResult() {
        return result;
    }

    /**
     * @param result the result to set
     */
    public void setResult(int result) {
        this.result = result;
    }
    
}
